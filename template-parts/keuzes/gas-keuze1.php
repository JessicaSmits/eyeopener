        <h4>Waarom vervangen?</h4>
        <p>Vanaf 2050 wilt de overheid Co2 neutraal zijn. Dit betekent dat vanaf 2050 je in principe niet meer kan koken op gas, wat betekent dat je voor die tijd van het gas af moet zijn. Een inductieplaat is een van de mogelijkheden om elektrisch te koken. Op het moment zijn de kosten van koken op elektrisch dan op gas, echter wordt de gasprijs steeds duurder en de prijs van de elektriciteit steeds goedkoper waardoor de verwachting is dat je binnen 20-30 jaar dit terugverdiend hebt.</p>
        <p>Bij inductie koken wordt niet de kookplaat, maar de pan zelf opgewarmd. Als de kookplaat aanstaat maar er geen pan op staat, gebeurt er dus niks. Haal je na het koken je pan van de plaat af, dan schakelt hij automatisch uit. Na het koken is er wel restwarmte aanwezig op de kookplaat, maar die koelt snel af. Daarbij bespaar je bij een inductieplaat meer energie dan bij een keramische kookplaat.</p>

        <h4>De werking</h4>
        <p>Bij de inductiekookplaat zorgt een elektromagnetisch veld voor de verwarming. Na het inschakelen van de kookplaat zie je geen verwarmingselement opgloeien en wordt de plaat ook niet warm. De warmte wordt pas na het plaatsen van een geschikte pan direct in de panbodem opgewekt. Je hebt wel geschikte pannen nodig voor een inductieplaat.</p>

