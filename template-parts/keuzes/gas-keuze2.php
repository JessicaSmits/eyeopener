        <h4>Waarom vervangen?</h4>
        <p>Een keramische kookplaat werkt op elektriciteit. Met een blik op de toekomst is dit een prima optie. In 2050 wilt de overheid namelijk CO2 neutraal zijn, wat betekent dat je in principe niet meer kan koken op gas. Een keramische kookplaat is dan dus wel toegestaan. Koken op een keramische kookplaat is net wat duurder dan koken op gas, maar aangezien het gas steeds duurder wordt en de elektriciteit steeds goedkoper zijn de verwachtingen dat je dit terug hebt verdiend binnen 20-30 jaar. </p>


        <h4>De werking</h4>
        <p>De keramische kookplaat lijkt qua uiterlijk erg op een inductie kookplaat, alleen de werking is heel anders. De kookzones op een keramische plaat zullen moeten opwarmen. Dit is ook te zien aan de zones, die steeds meer beginnen te gloeien. Wanneer je de pan van de kookzone afhaalt, zal de kookzone tijd nodig hebben om af te koelen. Een keramische kookplaat is minder energie zuinig dat een inductieplaat.</p>

