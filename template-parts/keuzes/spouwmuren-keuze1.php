        <h4>Waarom vervangen?</h4>
        <p>Huizen die na 1920 zijn gebouwd, hebben meestal een spouwmuur. Een spouwmuur is een muur die uit twee evenwijdige delen bestaat, een buiten- en binnenmuur, ook wel buiten- en binnenblad genoemd. De open ruimte tussen de beide muren wordt de spouw genoemd. De spouw voorkomt vochtdoorslag van buiten naar binnen en heeft ook een isolerende functie.</p>
        <p>Echter, wanneer er geen isolatiemateriaal in de spouw is aangebracht, zorgen de luchtstromingen ervoor dat de spouw koel blijft. Dit betekent dan ook dat de binnenmuur koude afgeeft aan de binnenkant van de woning. Dit heeft tot gevolg dat het binnen sneller koud wordt én dat er koude lucht gaat stromen wat aanvoelt als tocht.</p>

        <h4>De werking</h4>
        <p>Door de spouwmuur kan het regenwater dat tegen de gevel komt niet meer door de <a href="https://nl.wikipedia.org/wiki/Capillariteit">capillaire werking</a> van de poreuze baksteen tot binnen doordringen, doordat de gevel onderbroken is door de spouw. Door de spouw licht te ventileren met roostertjes onder en boven in het buitenblad wordt de waterdamp afgevoerd en droogt de muur. Bij onderzoek is echter gebleken dat bij normale bakstenen het vocht in de muur voor ca 98% door middel van diffusie het buitenblad verlaat. Met andere woorden, het ventileren van de spouw is overbodig.</p>

